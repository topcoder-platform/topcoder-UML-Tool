package com.topcoder.uml.actions.general.operationtypes;

/**
 * <p>
 * This enumeration representing the type of change z-order operation.
 * </p>
 *
 * @author kinzz, rainday
 * @version 1.0
 */
public enum ChangeZOrderOperationType {
    /**
     * <p>
     * Represents the change z-order operation type: forward.
     * </p>
     */
    FORWARD,

    /**
     * <p>
     * Represents the change z-order operation type: backward.
     * </p>
     */
    BACKWARD,

    /**
     * <p>
     * Represents the change z-order operation type: to front.
     * </p>
     */
    TOFRONT,

    /**
     * <p>
     * Represents the change z-order operation type: to back.
     * </p>
     */
    TOBACK
}
